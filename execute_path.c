#include "minishell.h"
#include "libft/libft.h"
#include <unistd.h>

static char	*check_absolute_path(char *command)
{
	if (ft_strchr(command, '/'))
	{
		if (access(command, X_OK) == 0)
			return (ft_strdup(command));
		return (NULL);
	}
	return (NULL);
}

static char	*search_in_paths(char *command, char **paths)
{
	char	*full_path;
	int		i;

	i = 0;
	while (paths[i])
	{
		full_path = build_full_path(paths[i], command);
		if (full_path && access(full_path, X_OK) == 0)
		{
			free_string_array(paths);
			return (full_path);
		}
		free(full_path);
		i++;
	}
	free_string_array(paths);
	return (NULL);
}

char	*find_command_path(char *command, t_base *base)
{
	char	*path_env;
	char	**paths;
	char	*result;

	result = check_absolute_path(command);
	if (result)
		return (result);
	path_env = get_env_value(*base, "PATH");
	if (!path_env)
		return (NULL);
	paths = ft_split(path_env, ':');
	if (!paths)
		return (NULL);
	return (search_in_paths(command, paths));
}
